public class MathClasses {
    public static void main(String[] args) {

        System.out.println(Math.min(12.5, 7.5)); // can finding min value through two values
        System.out.println(Math.max(12.5, 7.5)); // can finding max value through two values
        System.out.println(Math.sqrt(25));
        System.out.println(Math.abs(12.5));
        System.out.println(Math.abs(-12.0));
        System.out.println(Math.pow(5, 3));
        System.out.println(Math.pow(10, 2));
        System.out.println(Math.ceil(12.5));
        System.out.println(Math.floor(12.5));

    }

}

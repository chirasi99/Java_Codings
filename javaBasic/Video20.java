public class Video20 {
    public static void main(String[] args) {
        int n = 0;
        while (n < 5) {
            System.out.println("Your name is Malan" + n);
            n++;
        }

        while (n >= 0) {
            System.out.println(n);
            n--;
        }

    }
}

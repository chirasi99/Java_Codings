//this is a program of using do while.....
public class Java21 {
    public static void main(String[] args) {
        int n = 0;
        do {
            System.out.println("Your name is " + n);
            n++;
        } while (n < 5);

        System.out.println("\n");

        do {
            System.out.println("Your name is " + n);

        } while (n > 5);
    }
}
